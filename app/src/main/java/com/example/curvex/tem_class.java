package com.example.curvex;

public class tem_class {
    String input,convert,to,result;

    public tem_class() {
    }

    public tem_class(String input, String convert, String to, String result) {
        this.input = input;
        this.convert = convert;
        this.to = to;
        this.result = result;
    }

    public String getInput() {
        return input;
    }

    public void setInput(String input) {
        this.input = input;
    }

    public String getConvert() {
        return convert;
    }

    public void setConvert(String convert) {
        this.convert = convert;
    }

    public String getTo() {
        return to;
    }

    public void setTo(String to) {
        this.to = to;
    }

    public String getResult() {
        return result;
    }

    public void setResult(String result) {
        this.result = result;
    }
}
